    private void iniciarJuegoFijo() {
        juegoAhorcadoFijo = new JuegoAhorcadoFijo(Main.adminPalabrasSecretas.getPalabraFija());
        actualizarInterfaz();
    }
    
    private void iniciarJuegoAzar() {
        juegoAhorcadoAzar = new JuegoAhorcadoAzar(adminPalabrasSecretas.getPalabras());
        actualizarInterfaz();
    }
    
    private void actualizarInterfaz() {
        if (juegoAhorcadoFijo != null) {
            PalabraOcultaLabel.setText(juegoAhorcadoFijo.getPalabraActual());
            IntentosLabel.setText(String.valueOf(juegoAhorcadoFijo.getIntentos()));
        } else if (juegoAhorcadoAzar != null) {
            PalabraOcultaLabel.setText(juegoAhorcadoAzar.getPalabraActual());
            IntentosLabel.setText(String.valueOf(juegoAhorcadoAzar.getIntentos()));
        }
    }
    
    private void jugar() {
        if (Main.adminPalabrasSecretas.isModoFijo() && !Main.adminPalabrasSecretas.isModoAzar()) {
            juegoAhorcadoFijo.jugar();
            PalabraOcultaLabel.setText(juegoAhorcadoFijo.getPalabraActual());
            IntentosLabel.setText(String.valueOf(juegoAhorcadoFijo.getIntentos()));
        } else if (Main.adminPalabrasSecretas.isModoAzar() && !Main.adminPalabrasSecretas.isModoFijo()) {
            juegoAhorcadoAzar.jugar();
            PalabraOcultaLabel.setText(juegoAhorcadoAzar.getPalabraActual());
            IntentosLabel.setText(String.valueOf(juegoAhorcadoAzar.getIntentos()));
        }
            
            if (juegoAhorcadoFijo != null && juegoAhorcadoFijo.isPartidaGanada()) {
                Main.adminPalabrasSecretas.setModoFijo(false);
                Main.adminPalabrasSecretas.setModoAzar(false);
                juegoAhorcadoFijo.setPartidaGanada(false);
                MenuPrincipal menuPrincipal = new MenuPrincipal();
                menuPrincipal.setVisible(true);
                this.setVisible(false);
            } else if (juegoAhorcadoAzar != null && juegoAhorcadoAzar.isPartidaGanada()) {
                Main.adminPalabrasSecretas.setModoFijo(false);
                Main.adminPalabrasSecretas.setModoAzar(false);
                juegoAhorcadoAzar.setPartidaGanada(false);
                MenuPrincipal menuPrincipal = new MenuPrincipal();
                menuPrincipal.setVisible(true);
                this.setVisible(false);
            }
            
            actualizarIntentos();
        
    }
    
